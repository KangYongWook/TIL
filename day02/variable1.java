package day02;

public class variable1 {
	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		double d =123.456789;
		
		System.out.printf("\t result = %d %d \n",a,b);
		System.out.printf("\t result = %o \n",a); // 8진수
		System.out.printf("\t result = %x \n",a); // 16진수
		System.out.printf("result = %10.3f \n",d); // 16진수
		System.out.println(d);
	}

}
